// 设置扫描方向
static void ILI9341_GramScan(uint8_t ucOption)
{
    uint16_t reg = 0;
    switch (ucOption) {
        case 0: reg = 0x00; g_LCD_LenX = ILI9341_LESS_PIXEL; g_LCD_LenY = ILI9341_MORE_PIXEL; break;
        case 1: reg = 0x20; g_LCD_LenX = ILI9341_MORE_PIXEL; g_LCD_LenY = ILI9341_LESS_PIXEL; break;
        case 2: reg = 0x40; g_LCD_LenX = ILI9341_LESS_PIXEL; g_LCD_LenY = ILI9341_MORE_PIXEL; break;
        case 3: reg = 0x60; g_LCD_LenX = ILI9341_MORE_PIXEL; g_LCD_LenY = ILI9341_LESS_PIXEL; break;
        case 4: reg = 0x80; g_LCD_LenX = ILI9341_LESS_PIXEL; g_LCD_LenY = ILI9341_MORE_PIXEL; break;
        case 5: reg = 0xA0; g_LCD_LenX = ILI9341_MORE_PIXEL; g_LCD_LenY = ILI9341_LESS_PIXEL; break;
        case 6: reg = 0xC0; g_LCD_LenX = ILI9341_LESS_PIXEL; g_LCD_LenY = ILI9341_MORE_PIXEL; break;
        case 7: reg = 0xE0; g_LCD_LenX = ILI9341_MORE_PIXEL; g_LCD_LenY = ILI9341_LESS_PIXEL; break;
        default: reg = 0x40; g_LCD_LenX = ILI9341_LESS_PIXEL; g_LCD_LenY = ILI9341_MORE_PIXEL; break;
    }
    ILI9341_WriteCmd(CMD_MAC);
    ILI9341_WriteData(reg);
}

// 打开窗口
static void ILI9341_OpenWindow(uint16_t usX, uint16_t usY, uint16_t usWidth, uint16_t usHeight)
{
    uint16_t x1, x2, y1, y2;
    x1 = usX;
    x2 = usX + usWidth - 1;
    y1 = usY;
    y2 = usY + usHeight - 1;
    ILI9341_WriteCmd(CMD_COLUMN_ADDR);
    ILI9341_WriteData(x1 >> 8);
    ILI9341_WriteData(x1 & 0xFF);
    ILI9341_WriteData(x2 >> 8);
    ILI9341_WriteData(x2 & 0xFF);

    ILI9341_WriteCmd(CMD_PAGE_ADDR);
    ILI9341_WriteData(y1 >> 8);
    ILI9341_WriteData(y1 & 0xFF);
    ILI9341_WriteData(y2 >> 8);
    ILI9341_WriteData(y2 & 0xFF);

    ILI9341_WriteCmd(CMD_GRAM);
}

