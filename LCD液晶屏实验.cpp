// 画线（简单 Bresenham）
void LCD_DrawLine(uint16_t usX1, uint16_t usY1, uint16_t usX2, uint16_t usY2)
{
    int dx = (usX2 > usX1) ? (usX2 - usX1) : (usX1 - usX2);
    int dy = (usY2 > usY1) ? (usY2 - usY1) : (usY1 - usY2);
    int stepx = (usX2 > usX1) ? 1 : -1;
    int stepy = (usY2 > usY1) ? 1 : -1;
    int err = dx - dy;
    int x = usX1, y = usY1;
    while (1) {
        LCD_SetPixel(x, y);
        if (x == usX2 && y == usY2) break;
        int e2 = 2 * err;
        if (e2 > -dy) { err -= dy; x += stepx; }
        if (e2 < dx)  { err += dx; y += stepy; }
    }
}

// 画矩形
void LCD_DrawRectangle(uint16_t usX_Start, uint16_t usY_Start, uint16_t usWidth, uint16_t usHeight, uint8_t ucFilled)
{
    if (ucFilled) {
        ILI9341_OpenWindow(usX_Start, usY_Start, usWidth, usHeight);
        ILI9341_FillColor((uint32_t)usWidth * usHeight, g_LCD_TextColor);
    } else {
        LCD_DrawLine(usX_Start, usY_Start, usX_Start + usWidth - 1, usY_Start);
        LCD_DrawLine(usX_Start + usWidth - 1, usY_Start, usX_Start + usWidth - 1, usY_Start + usHeight - 1);
        LCD_DrawLine(usX_Start + usWidth - 1, usY_Start + usHeight - 1, usX_Start, usY_Start + usHeight - 1);
        LCD_DrawLine(usX_Start, usY_Start + usHeight - 1, usX_Start, usY_Start);
    }
}

// 画圆（简单八分法）
void LCD_DrawCircle(uint16_t usX_Center, uint16_t usY_Center, uint16_t usRadius, uint8_t ucFilled)
{
    int x = 0, y = usRadius;
    int d = 3 - 2 * usRadius;
    if (ucFilled) {
        // 填充圆：逐行扫描，但简单实现使用水平线
        for (int i = -usRadius; i <= usRadius; i++) {
            int h = (int)sqrt(usRadius * usRadius - i * i);
            LCD_DrawLine(usX_Center - h, usY_Center + i, usX_Center + h, usY_Center + i);
        }
    } else {
        while (x <= y) {
            LCD_SetPixel(usX_Center + x, usY_Center + y);
            LCD_SetPixel(usX_Center - x, usY_Center + y);
            LCD_SetPixel(usX_Center + x, usY_Center - y);
            LCD_SetPixel(usX_Center - x, usY_Center - y);
            LCD_SetPixel(usX_Center + y, usY_Center + x);
            LCD_SetPixel(usX_Center - y, usY_Center + x);
            LCD_SetPixel(usX_Center + y, usY_Center - x);
            LCD_SetPixel(usX_Center - y, usY_Center - x);
            if (d < 0) d += 4 * x + 6;
            else { d += 4 * (x - y) + 10; y--; }
            x++;
        }
    }
}

// 显示英文字符串（8x16字体）
void LCD_DispStringEN(uint16_t usX, uint16_t usY, uint8_t uDir, char *pStr)
{
    // uDir 未使用，保持横向显示
    uint16_t i = 0;
    uint16_t x = usX, y = usY;
    while (pStr[i] != '\0') {
        if (pStr[i] < 32 || pStr[i] > 126) {
            i++;
            continue;
        }
        const unsigned char *font = ASCII8x16[pStr[i] - 32];
        // 画字符 8x16
        for (uint16_t row = 0; row < 16; row++) {
            uint8_t byte = font[row];
            for (uint16_t col = 0; col < 8; col++) {
                if (byte & (0x80 >> col)) {
                    LCD_SetPixel(x + col, y + row);
                } else {
                    // 背景色（如需清背景则写背景色，但为了速度此处不处理）
                    // 若需背景，可在此处写背景色，但会减慢速度，推荐先清区域
                }
            }
        }
        // 移动下一个字符位置（加间距1）
        x += 9;
        if (x + 8 > g_LCD_LenX) {
            x = usX;
            y += 17;
        }
        i++;
    }
}

